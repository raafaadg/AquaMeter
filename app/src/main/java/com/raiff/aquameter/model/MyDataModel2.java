package com.raiff.aquameter.model;

public class MyDataModel2 {

    private String id;
    private String time;
    private String gps;
    private String data;



    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

    public String getTime() {
        return time;
    }

    public void setTime(String time) {
        this.time = time;
    }

    public String getGps() {
        return gps;
    }

    public void setGps(String gps) {
        this.gps = gps;
    }

    public String getData() {
        return data;
    }

    public void setData(String data) {
        this.data = data;
    }

}